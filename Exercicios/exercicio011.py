# Calculando a área e quantos baldes necessários para pintar essa área

largura = float(input('Insira o valor da largura: '))
altura = float(input('Insira o valor da altura: '))

print(f'Area: {largura*altura}m²')
print(f'Seriam necessarios {(largura*altura)/2} baldes de tinta para pintar essa area')